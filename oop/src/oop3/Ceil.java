package oop3;
 
public class Ceil {
 
  public static void main(String[] args) {
    System.out.println(Math.ceil(1.5));
    System.out.println(Math.ceil(0.1));
    System.out.println(Math.ceil(0.001));
    
    System.out.println(Math.ceil(-1.5));
    System.out.println(Math.ceil(-0.1));
    System.out.println(Math.ceil(-0.001));
  }
}