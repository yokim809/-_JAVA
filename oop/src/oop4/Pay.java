package oop4;
 
public class Pay {
  String name;
  int basic;
  int year;
  int sudang;
  int salary;
  int total;
  
  public Pay() {
 
  }
 
  public Pay(String name, int basic, int year, int sudang, int salary, int total) {
    this.name = name;
    this.basic = basic;
    this.year = year;
    this.sudang = sudang;
    this.salary = salary;
    this.total = total;
  }
  
 
}