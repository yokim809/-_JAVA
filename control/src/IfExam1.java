
public class IfExam1 {

  public static void main(String[] args) {
    int su = Integer.parseInt(args[0]);

    if (su % 2 ==0) {
      System.out.println("2의 배수");  
    } 
    if (su % 3 ==0) {
      System.out.println("3의 배수");
    }
    if (su % 4 ==0) {
      System.out.println("4의 배수");
    }
    if (su % 5 ==0) {
      System.out.println("5의 배수");
    }
    
    
    
    
  }

}
